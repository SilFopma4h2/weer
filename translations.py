def get_weather_description(weather_code: int, language: str = "nl") -> str:
    """Get weather description in specified language"""
    if language not in WEATHER_CODE_TRANSLATIONS:
        language = "nl"  # fallback to Dutch
    
    return WEATHER_CODE_TRANSLATIONS[language].get(weather_code, 
        UI_TRANSLATIONS[language].get("unknown_weather", "unknown weather"))

def get_ui_text(key: str, language: str = "nl") -> str:
    """Get UI text in specified language"""
    if language not in UI_TRANSLATIONS:
        language = "nl"  # fallback to Dutch
    
    return UI_TRANSLATIONS[language].get(key, key)